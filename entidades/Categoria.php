<?php
Class Categoria{
    private $idCategoria;
    private $nombre;

    function get_idCategoria(){
        $this->idCategoria;
    }
    function set_idCategoria($idCategoria){
        $this->idCategoria=$idCategoria;
    }
    function get_nombre(){
        $this->nombre;
    }
    function set_nombre($nombre){
        $this->nombre=$nombre;
    }


}



?>